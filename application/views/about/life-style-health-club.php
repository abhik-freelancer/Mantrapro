                <li>
                    <span style="color:#F8663F;font-weight:700;">A. Endurance Test:</span> Just exercising on treadmill does not help much if one does not know how hard or how fast one needs to do it. Through endurance test the fitness trainer can assess your endurance ability (ability to resist fatigue for prolong period of time) for a particular group of muscle as well as for the whole body.
                </li>
                <li>
                    <span style="color:#F8663F;font-weight:700;">a) General (cardiovascular) Endurance Test:</span> General endurance is the ability of an individual to resist fatigue over a prolong period of time. Your total body, particularly the cardiovascular and pulmonary system is examined through this test. Fitness trainers need this test result to design and monitor a perfect cardiovascular workout routine for an individual.
                </li>

                <li><span class="badge">1</span>
                    <span style="color:#F8663F;font-weight:700;">Vo2 max:</span> Vo2max is considered to be the gold standard by which one can measure his or her cardio respiratory fitness level (endurance). A person who is fit in the cardio respiratory sense of the word "fit", would have a higher VO2MAX than someone who is less fit. What it means is that your body is more able to absorb and use oxygen to generate energy for your muscle, and this will certainly give you an edge.
                </li>
                <li><span class="badge">2</span>
                    <span style="color:#F8663F;font-weight:700;">METs:</span> METs measure how hard your body is working. The harder your body works, the more oxygen it would burn to release the energy it needs to work and higher will be the METs level. Medical studies suggest improving your METs level may save your life. At complete rest, on an average, METs level will be 1.0 or 1 MET.
                </li>

                <li>
                    <span style="color:#F8663F;font-weight:700;">b) Muscular Endurance Test:</span> Muscular endurance is the ability of a muscle to resist fatigue over a prolong period of time. Through muscular endurance test we will assess your muscular ability to perform a specific work for a prolong period of time.
                </li>

            </ul>
        </div>

        <div class="col-md-6 health-style-block4">

