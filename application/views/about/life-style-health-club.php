                </li>
                <li><span class="badge">2</span>
                    <span style="color:#F8663F;font-weight:700;">Waist Circumference:</span> Evaluating fat in the abdominal region is important in determining an individual's health risk. Fat in the abdominal region, known as visceral fat, is associated with greater risk of Type2 diabetes, hypertension, cardiovascular disease and dyslipidemia. Standards are as follows - Men: no greater than 40 inches; Women: no greater than 34.6 inches. Ost-menopause women: No greater than 43.3 inches.
                </li>
            </ul>
        </div>

        <div class="col-md-6 health-style-block5">

            <ul class="">
                <li><span class="badge">3</span>
                    <span style="color:#F8663F;font-weight:700;">Waist hip ratio:</span> It is not only the amount of body fat one is having, but also the area where such fat is stored in the body which has a direct impact on one's health. Adults who store most of their body fat around their waists have an increased risk of high blood pressure, type 2 diabetes, heart disease and stroke compared to those who have the same amount of body fat stored around their hips and thighs. Measuring one's waist-to-hip ratio is an easy way to see how much weight one is carrying around his or her abdomen as opposed to around the hips. It is designed for people aged 18 years and above. A healthy waist hip ratio for women is 0.8 or lower. A healthy ratio for men is 1.0 or lower. The National Institute of Diabetes, Digestive and Kidney Diseases (NIDDK) states that women with waist-hip ratios of more than 0.8, and men with more than 1.0, are at increased health risk because of their body fat distribution pattern.
                </li>
