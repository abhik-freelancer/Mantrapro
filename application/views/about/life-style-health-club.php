                </li>

                <li><span class="badge">1</span>
                    <span style="color:#F8663F;font-weight:700;">Minimum Muscular Strength Test:</span> It is commonly known as the Kraus-Weber Test. This test measures the level of strength and flexibility of certain key muscle groups. Below such level the functioning of the whole body of a healthy individual seems to be endangered.
                </li>
                <li><span class="badge">2</span>
                    <span style="color:#F8663F;font-weight:700;">One Repetition Maximum (1RM):</span> 1RM is generally considered to be the best method of measuring strength. 1RM refers to the amount of weight one can lift at one time and one time only. 1RM testing is a very important procedure as it helps to determine specific load settings for any given training routine. It also enables one to determine the efficiency of the training program and assess the progress he or she has made.
                </li>

                <li>
                    <span style="color:#F8663F;font-weight:700;">C. Flexibility Test:</span> With strength and endurance training our muscles become tight and stiff. It puts immense pressure on the joints and one is prone to get injured easily. Flexibility test helps is assessing how flexible one is and areas which require improvement.
